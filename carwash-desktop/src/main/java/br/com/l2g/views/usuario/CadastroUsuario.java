    private void pesquisarGrupoPermissaoporId() {
       try {
            if (!codGroupPermissaoText.getText().equals("")) {
                String URL_PESQ = URL_GRUPO_PERMISSAO + "/" + codGroupPermissaoText.getText().trim();
                HttpGet get = new HttpGet(URL_PESQ);
                String resposta = Util.enviaRequest(get);
                Optional retorno = Optional.ofNullable(Util.jsonToObject(resposta, GrupoPermissao.class));                
                if (retorno.isPresent()) {
                    GrupoPermissao cidade = (GrupoPermissao) retorno.get();
                    nomeGroupPermissaoText.setText(cidade.getDescricaoGrupoPermissao());
                } else {
                    JOptionPane.showMessageDialog(null, "Registro nao encontrado!", "ERRO", JOptionPane.ERROR_MESSAGE);
                    codGroupPermissaoText.requestFocusInWindow();
                }
            }
        } catch (IOException | HttpException | NoSuchPaddingException | IllegalBlockSizeException | NoSuchAlgorithmException | BadPaddingException | InvalidKeyException ex) {
            Logger.getLogger(CadastroUsuario.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, ex.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }

    void enviarCodigoSelecionado(String id) {
        try {
            String URL_PESQ = URL_USUARIO + "/" + id;
            HttpGet get = new HttpGet(URL_PESQ);
            String resposta = Util.enviaRequest(get);
            Optional retorno = Optional.ofNullable(Util.jsonToObject(resposta, Usuario.class));
            if (retorno.isPresent()) {
                Usuario usuario = (Usuario) retorno.get();

                codGroupPermissaoText.setText(String.valueOf(usuario.getGrupoPermissao().getCodigoGrupoPermissao()));
                nomeGroupPermissaoText.setText(usuario.getGrupoPermissao().getDescricaoGrupoPermissao());

                codigoUsuarioText.setText(String.valueOf(usuario.getIdUsuario()));
                nomeText.setText(usuario.getNome());
                loginText.setText(usuario.getLogin());
                passwordText.setText(usuario.getSenha());

            }

        } catch (IOException | HttpException | NoSuchPaddingException | IllegalBlockSizeException | NoSuchAlgorithmException | BadPaddingException | InvalidKeyException ex) {
            Logger.getLogger(CadastroUsuario.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, ex.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }

