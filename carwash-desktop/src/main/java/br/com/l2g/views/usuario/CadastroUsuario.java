    private void salvarUsuario() {
        try {
            HttpPost post = new HttpPost(URL_USUARIO);
            Usuario empresa = criaUsuario();
            String jsonEnvio = Util.objectToJson(empresa);
            post.setEntity(new StringEntity(jsonEnvio));
            String jsonResposta = Util.enviaRequest(post);
            Optional retorno = Optional.ofNullable(Util.jsonToObject(jsonResposta, Usuario.class));
            if (!retorno.isPresent()) {
                JOptionPane.showMessageDialog(null, "Erro ao salvar registro!", "ERRO", JOptionPane.ERROR_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Registro salvo com sucesso!", "Aviso", JOptionPane.INFORMATION_MESSAGE);
                dispose();
            }

        } catch (IOException | HttpException | NoSuchPaddingException | IllegalBlockSizeException | NoSuchAlgorithmException | BadPaddingException | InvalidKeyException ex) {
            Logger.getLogger(CadastroUsuario.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, ex.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }

    private Usuario criaUsuario() {

        GrupoPermissao grupoPermissao = new GrupoPermissao();
        grupoPermissao.setCodigoGrupoPermissao(Integer.parseInt(codGroupPermissaoText.getText()));
        grupoPermissao.setDescricaoGrupoPermissao(nomeGroupPermissaoText.getText().trim());

        Usuario usuario = new Usuario();
        
        if (!codigoUsuarioText.getText().trim().isEmpty()) {
            usuario.setIdUsuario(Integer.parseInt(codigoUsuarioText.getText().trim()));
        }

        usuario.setNome(nomeText.getText().trim().toUpperCase());
        usuario.setLogin(loginText.getText().trim());
        usuario.setSenha(Util.geraMD5(new String(passwordText.getPassword())));
        usuario.setGrupoPermissao(grupoPermissao);

        return usuario;
    }

